def _dedupe_companies(entries: list[tuple[str, list[str], list[str]]]) -> list[dict]:
    seen: set[str] = set()
    result = []
    for name, domains, aliases in entries:
        key = re.sub(r"[^a-z0-9]", "", name.lower())
        if key in seen:
            continue
        seen.add(key)
        entry: dict = {"name": name}
        if domains:
            entry["domains"] = sorted(set(domains))
        if aliases:
            entry["aliases"] = sorted(set(aliases))
        result.append(entry)
    return result


def build_fortune500() -> list[dict]:
    combined: list[tuple[str, list[str], list[str]]] = list(FORTUNE_500)
    for name, domain, aliases in FORTUNE_500_EXTRA_NAMES:
        combined.append((name, [domain] if domain else [], aliases))
    return _dedupe_companies(combined)


def build_institutions(data: list[tuple]) -> list[dict]:
    return _dedupe_companies(data)


def write_yaml(path: Path, header: str, body: dict) -> None:
    lines = [f"# {line}" for line in header.strip().split("\n")]
    lines.append("")
    content = "\n".join(lines) + "\n" + yaml.dump(body, sort_keys=False, allow_unicode=True)
    path.write_text(content, encoding="utf-8")
    print(f"Wrote {path} ({len(body.get('companies') or body.get('institutions') or body.get('organizations', []))} entries)")


